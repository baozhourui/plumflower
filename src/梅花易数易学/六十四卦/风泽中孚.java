public class 风泽中孚 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("巽上兑下\n");
    }

    @Override
    public void 卦辞() {
        println("豚鱼，吉。利涉大川，利贞。\n");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初九，虞吉，有它不燕。\n" +
                "九二，鸣鹤在阴，其子和之。我有好爵，吾与尔靡之。\n" +
                "六三，得敌，或鼓或罢，或泣或歌。\n" +
                "六四，月几望，马匹亡，无咎。\n" +
                "九五，有孚挛如，无咎。\n" +
                "上九，翰音登于天，贞凶。");
    }
}

