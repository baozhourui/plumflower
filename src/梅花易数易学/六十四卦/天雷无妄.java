public class 天雷无妄 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("乾上震下\n");
    }

    @Override
    public void 卦辞() {
        println("元亨，利贞。其匪正有眚，不利有攸往。\n");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初九，无妄往，吉。\n" +
                "六二，不耕获，不菑畲，则利用攸往。\n" +
                "六三，无妄之灾，或系之牛，行人之得，邑人之灾。\n" +
                "九四，可贞。无咎。\n" +
                "九五，无妄之疾，勿药有喜。\n" +
                "上九，无妄行，有眚，无攸利。 ");
    }
}
