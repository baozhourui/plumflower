public class 泽水困 extends 六十四卦{
    @Override
    public void 卦意() {
        println("兑上坎下\n");
    }

    @Override
    public void 卦辞() {
        println("亨。贞大人吉，无咎。有言不信。");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初六，臀困于株木，入于幽谷，三岁不觌。\n" +
                "九二，困于酒食，朱绂方来。利用享祀。征凶，无咎。\n" +
                "六三，困于石，据于蒺藜，入于其宫，不见其妻，凶。\n" +
                "九四，来徐徐，困于金车，吝，有终。\n" +
                "九五，劓刖，困于赤绂，乃徐有说，利用祭祀。\n" +
                "上六，困于葛藟，于臲<臬兀>，曰动悔有悔，征吉。");
    }
}
