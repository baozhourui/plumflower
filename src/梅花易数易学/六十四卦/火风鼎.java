public class 火风鼎 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("离上巽下\n");
    }

    @Override
    public void 卦辞() {
        println("元吉，亨");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初六：鼎颠趾，利出否，得妾以其子，无咎。\n" +
                "九二：鼎有实，我仇有疾，不我能即，吉。\n" +
                "九三：鼎耳革，其行塞，雉膏不食，方雨亏悔，终吉。\n" +
                "九四：鼎折足，覆公餗，其形渥，凶。\n" +
                "六五：鼎黄耳金铉，利贞。\n" +
                "上九：鼎玉铉，大吉，无不利。");
    }
}
