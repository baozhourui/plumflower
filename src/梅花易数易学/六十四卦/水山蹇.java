package 梅花易数易学.六十四卦;

import static Print.Print.println;

/**
 * Created by zyf on 2016/9/15.
 */
public class 水山蹇 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("坎上艮下\n");
    }

    @Override
    public void 卦辞() {
        println("利西南，不利东北。利见大人。贞吉。");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初六，往蹇来誉。\n" +
                "六二，王臣蹇蹇，匪躬之故。\n" +
                "九三，往蹇来反。\n" +
                "六四，往蹇来连。\n" +
                "九五，大蹇朋来。\n" +
                "上六，往蹇来硕，吉，利见大人。");
    }
}
