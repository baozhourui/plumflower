package 梅花易数易学.六十四卦;

import static Print.Print.println;

/**
 * Created by zyf on 2016/9/15.
 */
public class 火水未济 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("离上坎下\n");
    }

    @Override
    public void 卦辞() {
        println("亨。小狐汔济，濡其尾，无攸利。");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初六，濡其尾，吝。\n" +
                "九二，曳其轮，贞吉。\n" +
                "六三，未济，征凶。利涉大川。\n" +
                "九四，贞吉，悔亡，震用伐鬼方，三年，有赏于大国。\n" +
                "六五，贞吉，无悔。君子之光，有孚吉。\n" +
                "上九，有孚于饮酒，无咎。濡其首，有孚失是。");
    }
}

