public class 震为雷 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("震上震下");
    }

    @Override
    public void 卦辞() {
        println("亨。震来虩虩，笑言哑哑，震惊百里，不丧匕鬯");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初九，震来虩虩，后笑言哑哑，吉。\n" +
                "六二，震来厉，亿丧贝，跻于九陵，勿逐，七日得。\n" +
                "六三，震苏苏，震行无眚。\n" +
                "九四，震遂泥。\n" +
                "六五，震往来，厉，意无丧，有事。\n" +
                "上六，震索索，视矍矍，征凶。震不于其躬，于其邻，无咎。婚媾有言。 ");
    }
}

