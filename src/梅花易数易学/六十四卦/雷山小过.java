package 梅花易数易学.六十四卦;

import static Print.Print.println;

/**
 * Created by zyf on 2016/9/15.
 */
public class 雷山小过 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("震上艮下");
    }

    @Override
    public void 卦辞() {
        println("亨。利贞。可小事，不可大事。飞鸟遗之音，不宜上，宜下，大吉。");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初六，飞鸟以凶。\n" +
                "六二，过其祖，遇其妣。不及其君，遇其臣。无咎。\n" +
                "九三，弗过防之，从或戕之，凶。\n" +
                "九四，无咎。弗过遇之，往厉必戒，勿用永贞。\n" +
                "六五，密云不雨，自我西郊。公弋取彼在穴。\n" +
                "上六，弗遇过之，飞鸟离之，凶，是谓灾眚。");
    }
}

