public class 兑为泽 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("兑上兑下");
    }

    @Override
    public void 卦辞() {
        println("亨。利贞");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初九，和兑，吉。\n" +
                "九二，孚兑，吉，悔亡。\n" +
                "六三，来兑，凶。\n" +
                "九四，商兑未宁，介疾有喜。\n" +
                "九五，孚于剥，有厉。\n" +
                "上六，引兑。");
    }
}

