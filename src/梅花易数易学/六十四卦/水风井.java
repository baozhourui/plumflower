public class 水风井 extends 六十四卦{
    @Override
    public void 卦意() {
        println("坎上巽下\n");
    }

    @Override
    public void 卦辞() {
        println("改邑不改井，无丧无得。往来井井。汔至，亦未繘井，羸其瓶，凶。");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初六，井泥不食。旧井无禽。\n" +
                "九二，井谷射鲋，瓮敝漏。\n" +
                "九三，井渫不食，为我心恻。可用汲，王明并受其福。\n" +
                "六四，井甃，无咎。\n" +
                "九五，井洌，寒泉食。\n" +
                "上六，井收勿幕，有孚元吉。 ");
    }
}
