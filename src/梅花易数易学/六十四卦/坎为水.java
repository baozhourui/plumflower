public class 坎为水 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("坎上坎下\n");
    }

    @Override
    public void 卦辞() {
        println("有孚维心，亨。行有尚");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初六，习坎，入于坎，窞，凶。\n" +
                "九二，坎有险，求小得。\n" +
                "六三，来之坎，坎险且枕，入于坎，窞，勿用。\n" +
                "六四，樽酒簋贰用缶，纳约自牖，终无咎。\n" +
                "九五，坎不盈，祗既平，无咎。\n" +
                "上六，系用徽纆，窴于丛棘，三岁不得，凶。");
    }
}
