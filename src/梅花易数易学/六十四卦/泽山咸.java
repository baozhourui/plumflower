public class 泽山咸 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("兑上艮下");
    }

    @Override
    public void 卦辞() {
        println("亨。利贞。取女吉。");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初六，咸其拇。\n" +
                "六二，咸其腓，凶。居吉。\n" +
                "九三，咸其股，执其随，往吝。\n" +
                "九四，贞吉。悔亡。憧憧往来，朋从尔思。\n" +
                "九五，咸其脢，无悔。\n" +
                "上六，咸其辅颊舌。");
    }
}
