import static Print.Print.println;

/**
 * Created by zyf on 2016/9/15.
 */
public class 天泽履 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("乾上兑下");
    }

    @Override
    public void 卦辞() {
        println("履虎尾，不咥人。亨。");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初九，素履往，无咎。\n" +
                "九二，履道坦坦，幽人贞吉。\n" +
                "六三，眇能视，跛能履，履虎尾，咥人，凶。武人为于大君。\n" +
                "九四，履虎尾，愬愬，终吉。\n" +
                "九五，夬履，贞厉。\n" +
                "上九，视履考祥，其旋元吉。");
    }
}
