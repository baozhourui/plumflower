public class 泽天夬 extends 六十四卦{
    @Override
    public void 卦意() {
        println("兑上乾下\n");
    }

    @Override
    public void 卦辞() {
        println("扬于王庭，孚号。有厉，告自邑。不利即戎，利有攸往。");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初九，壮于前趾，往不胜，为咎。\n" +
                "九二，惕号，莫夜有戎，勿恤。\n" +
                "九三，壮于頄，有凶。君子夬夬独行，遇雨若濡，有愠无咎。\n" +
                "九四，臀无肤，其行次且。牵羊悔亡，闻言不信。\n" +
                "九五，苋陆夬夬中行，无咎。\n" +
                "上六，无号，终有凶。");
    }
}
