public class 坤为地 extends 六十四卦{

    @Override
    public void 卦意() {
        println("坤上坤下");
    }

    @Override
    public void 卦辞() {
        println("元亨。利牝马之贞。君子有攸往，先迷，后得主，利。西南得朋，东北丧朋。安贞吉。");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初六：履霜，坚冰至。\n" +
                "六二，直、方、大，不习，无不利。\n" +
                "六三，含章，可贞，或从王事，无成有终。\n" +
                "六四：括囊﹔無咎，無譽。\n" +
                "六五，黄裳，元吉。\n" +
                "上六，龙战于野，其血玄黄。\n" +
                "用六，利永贞。");
    }
}
