public class 雷天大壮 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("震上乾下\n");
    }

    @Override
    public void 卦辞() {
        println("利贞。");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初九，壮于趾，征凶，有孚。\n" +
                "九二，贞吉。\n" +
                "九三，小人用壮，君子用罔，贞厉。羝羊触藩，羸其角。\n" +
                "九四，贞吉，悔亡。藩决不羸，壮于大舆之輹。\n" +
                "六五，丧羊于易，无悔。\n" +
                "上六，羝羊触藩，不能退，不能遂，无攸利，艰则吉。");
    }
}
