package 梅花易数易学.六十四卦;

import static Print.Print.println;

/**
 * Created by zyf on 2016/9/15.
 */
public class 泽火革 extends 六十四卦{
    @Override
    public void 卦意() {
        println("兑上离下\n");
    }

    @Override
    public void 卦辞() {
        println("已日乃孚。元亨。利贞，悔亡。");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初九，巩用黄牛之革。\n" +
                "六二，巳日乃革之，征吉，无咎。\n" +
                "九三，征凶。贞厉。革言三就，有孚。\n" +
                "九四，悔亡。有孚改命，吉。\n" +
                "九五，大人虎变，未占有孚。\n" +
                "上六，君子豹变，小人革面，征凶，居贞吉。");
    }
}
