package 梅花易数易学.六十四卦;

import static Print.Print.println;

/**
 * Created by zyf on 2016/9/15.
 */
public class 巽为风 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("巽上巽下");
    }

    @Override
    public void 卦辞() {
        println("小亨。利有攸往。利见大人");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初六，进退，利武人之贞。\n" +
                "九二，巽在床下，用史巫纷若，吉，无咎。\n" +
                "九三，频巽，吝。\n" +
                "六四，悔亡，田获三品。\n" +
                "九五，贞吉，悔亡，无不利，无初有终。先庚三日，后庚三日，吉。\n" +
                "上九，巽在床下，丧其资斧，贞凶。");
    }
}

