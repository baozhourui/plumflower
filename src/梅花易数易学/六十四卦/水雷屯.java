package 梅花易数易学.六十四卦;

import static Print.Print.println;

/**
 * Created by zyf on 2016/9/15.
 */
public class 水雷屯 extends 六十四卦{
    @Override
    public void 卦意() {
        println("坎上震下");
    }

    @Override
    public void 卦辞() {
        println("元亨，利贞。勿用有攸往。利建侯。");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初九，磐桓，利居贞。利建侯。\n" +
                "六二，屯如邅如，乘马班如。匪寇，婚媾。女子贞不字，十年乃字。\n" +
                "六三，即鹿无虞，惟入于林中，君子几不如舍，往吝。\n" +
                "六四，乘马班如，求婚媾。往吉，无不利。\n" +
                "九五，屯其膏，小，贞吉；大，贞凶。\n" +
                "上六，乘马班如，泣血涟如。");
    }
}
