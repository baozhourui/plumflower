package 梅花易数易学.六十四卦;

import static Print.Print.println;

/**
 * Created by zyf on 2016/9/15.
 */
public class 山水蒙 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("艮上坎下\n");
    }

    @Override
    public void 卦辞() {
        println("亨。匪我求童蒙，童蒙求我。初筮告，再三渎，渎则不告。利贞");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初六，发蒙，利用刑人，用说桎梏，以往吝。\n" +
                "九二，包蒙，吉。纳妇，吉。子克家。\n" +
                "六三，勿用取女，见金夫，不有躬。无攸利。\n" +
                "六四，困蒙，吝。\n" +
                "六五，童蒙，吉。\n" +
                "上九，击蒙，不利为寇，利御寇。");
    }
}
