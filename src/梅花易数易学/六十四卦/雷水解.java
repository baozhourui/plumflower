public class 雷水解 extends 六十四卦{
    @Override
    public void 卦意() {
        println("震上坎下");
    }

    @Override
    public void 卦辞() {
        println("利西南。无所往，其来复吉。有攸往，夙吉");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初六，无咎。\n" +
                "九二，田获三狐，得黄矢，贞吉。\n" +
                "六三，负且乘，致寇至，贞吝。\n" +
                "九四，解而拇，朋至斯孚。\n" +
                "六五，君子维有解，吉，有孚于小人。\n" +
                "上六，公用射隼于高墉之上，获之，无不利。");
    }
}
