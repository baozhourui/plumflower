public class 地雷复 extends 六十四卦{
    @Override
    public void 卦意() {
        println("坤上震下");
    }

    @Override
    public void 卦辞() {
        println("亨。出入无疾。朋来无咎。反覆其道，七日来复，利有攸往。");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初九，不远复，无祗悔，元吉。\n" +
                "六二，休复，吉。\n" +
                "六三，频复，厉，无咎。\n" +
                "六四，中行独复。\n" +
                "六五，敦复，无悔。\n" +
                "上六，迷复，凶，有灾眚。用行师，终有大败，以其国君凶，至于十年不克征。");
    }
}
