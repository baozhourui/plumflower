public class 天水讼 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("乾上坎下");
    }

    @Override
    public void 卦辞() {
        println("有孚窒惕，中吉，终凶。利见大人。不利涉大川。");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初六，不永所事，小有言，终吉。\n" +
                "九二，不克讼，归而逋。其邑人三百户，无眚。\n" +
                "六三，食旧德，贞厉，终吉。或从王事，无成。\n" +
                "九四，不克讼，复既命渝。安贞吉。\n" +
                "九五：讼，元吉。\n" +
                "上九：或锡之鞶带，终朝三褫之。");
    }
}
