public class 风水涣 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("巽上坎下\n");
    }

    @Override
    public void 卦辞() {
        println("亨。王假有庙。利涉大川，利贞。");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初六，用拯马壮，吉。\n" +
                "九二，涣奔其机，悔亡。\n" +
                "六三，涣其躬，无悔。\n" +
                "六四，涣其群，元吉。涣有丘，匪夷所思。\n" +
                "九五，涣汗其大号，涣王居，无咎。\n" +
                "上九，涣其血，去逖出，无咎。");
    }
}

