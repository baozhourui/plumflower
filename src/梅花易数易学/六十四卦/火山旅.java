public class 火山旅 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("离上艮下\n");
    }

    @Override
    public void 卦辞() {
        println("小亨。旅贞吉");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初六，旅琐琐，斯其所取灾。\n" +
                "六二，旅即次，怀其资，得童仆，贞。\n" +
                "九三，旅焚其次，丧其童仆，贞厉。\n" +
                "九四，旅于处，得其资斧，我心不快。\n" +
                "六五，射雉，一矢亡，终以誉命。\n" +
                "上九，鸟焚其巢，旅人先笑后号啕。丧牛于易，凶。");
    }
}

