public class 艮为山 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("艮上艮下\n");
    }

    @Override
    public void 卦辞() {
        println("艮其背，不获其身，行其庭，不见其人，无咎。");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初六，艮其趾，无咎。利永贞。\n" +
                "六二，艮其腓，不拯其随，其心不快。\n" +
                "九三，艮其限，列其夤，厉，熏心。\n" +
                "六四，艮其身，无咎。\n" +
                "六五，艮其辅，言有序，悔亡。\n" +
                "上九，敦艮，吉。 ");
    }
}

