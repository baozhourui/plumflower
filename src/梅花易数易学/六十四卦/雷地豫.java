public class 雷地豫 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("震上坤下\n");
    }

    @Override
    public void 卦辞() {
        println("利建侯行师。\n");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初六，鸣豫，凶。\n" +
                "六二，介于石，不终日，贞吉。\n" +
                "六三，盱豫，悔，迟有悔。\n" +
                "九四，由豫，大有得，勿疑。朋盍簪。\n" +
                "六五，贞疾，恒不死。\n" +
                "上六，冥豫，成有渝。无咎。");
    }
}
