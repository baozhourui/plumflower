package 梅花易数易学.六十四卦;

import static Print.Print.println;

/**
 * Created by zyf on 2016/9/15.
 */
public class 山泽损 extends 六十四卦{
    @Override
    public void 卦意() {
        println("艮上兑下\n");
    }

    @Override
    public void 卦辞() {
        println("有孚，元吉，无咎。可贞，利有攸往。曷之用？二簋可用享。\n");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初九，已事遄往，无咎。酌损之。\n" +
                "九二，利贞。征凶，弗损，益之。\n" +
                "六三，三人行则损一人，一人行则得其友。\n" +
                "六四，损其疾，使遄有喜，无咎。\n" +
                "六五，或益之十朋之龟，弗克违，元吉。\n" +
                "上九，弗损，益之，无咎，贞吉，利有攸往，得臣无家。");
    }
}
