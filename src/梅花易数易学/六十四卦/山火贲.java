public class 山火贲 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("艮上离下\n");
    }

    @Override
    public void 卦辞() {
        println("亨。小利有攸往。");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初九，贲其趾，舍车而徒。\n" +
                "六二，贲其须。\n" +
                "九三，贲如，濡如，永贞吉。\n" +
                "六四，贲如皤如，白马翰如。匪寇，婚媾。\n" +
                "六五，贲于丘园，束帛戋戋，吝，终吉。\n" +
                "上九，白贲，无咎。");
    }
}
