public class 山天大蓄 extends 六十四卦 {
    @Override
    public void 卦意() {
        println("艮上乾下\n");
    }

    @Override
    public void 卦辞() {
        println("利贞。不家食吉。利涉大川");
    }

    @Override
    public void 卦爻() {
        println();
    }

    @Override
    public void 六爻() {
        println("初九，有厉，利已。\n" +
                "九二，舆说輹。\n" +
                "九三，良马逐，利艰贞，曰闲舆卫，利有攸往。\n" +
                "六四，童牛之牿，元吉。\n" +
                "六五，□豕之牙，吉。\n" +
                "上九，何天之衢，亨。");
    }
}
